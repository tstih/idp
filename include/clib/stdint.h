#ifndef __STDINT_H__
#define __STDINT_H__

typedef char                            int8_t;
typedef unsigned char                   uint8_t;
typedef int                             int16_t;
typedef unsigned int                    uint16_t;
typedef long                            int32_t;
typedef unsigned long                   uint32_t;
typedef unsigned int*                   uintptr_t;

#endif /* __STDINT_H_ */